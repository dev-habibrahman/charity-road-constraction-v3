function getLang(){ return localStorage.getItem("lang") || "ar"; }
function setLang(l){ localStorage.setItem("lang", l); applyLang(); }
function t(k){ return I18N[getLang()][k] ?? k; }
function fmt(n){ return new Intl.NumberFormat(getLang()==="ar"?"ar-SA":"en-US").format(n); }

function applyLang(){
  const l = getLang();
  document.documentElement.lang = l;
  document.documentElement.dir = l==="ar" ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach(el => el.textContent = t(el.dataset.i18n));
  document.querySelectorAll("[data-i18n-ph]").forEach(el => el.placeholder = t(el.dataset.iphn||el.dataset.i18nPh));
  document.querySelectorAll("[data-fmt]").forEach(el => { const n=Number(el.dataset.fmt); el.textContent = fmt(n); });
  renderProjects();
  renderLive();
}

function renderProjects(){
  const root = document.getElementById("projects-grid");
  if(!root) return;
  root.innerHTML = PROJECTS.map((p,i)=>{
    const k = `p${i+1}`;
    const pct = Math.min(100, Math.round((p.raised/p.goal)*100));
    return `<article class="card project">
      <div class="ratio">
        <img src="${p.img}" alt="${t(k+'_title')}" loading="lazy">
        <span class="badge">${p.km} km</span>
      </div>
      <div class="card-body">
        <h3>${t(k+'_title')}</h3>
        <p style="font-size:.8rem;color:var(--muted);margin:0">${t(k+'_loc')}</p>
        <p style="margin-top:10px;font-size:.92rem">${t(k+'_desc')}</p>
        <div style="display:flex;justify-content:space-between;font-size:.78rem;font-weight:700;color:#4a4434">
          <span>${t('progress')}</span><span style="color:var(--primary)">${pct}%</span>
        </div>
        <div class="progress"><div style="width:${pct}%"></div></div>
        <div class="meta"><span><b style="color:var(--fg)">${fmt(p.raised)}</b> ${t('of')} ${fmt(p.goal)} ${t('currency')}</span></div>
      </div>
    </article>`;
  }).join("");

  const featured = document.getElementById("featured-project");
  if(featured){
    const p = PROJECTS[0]; const pct = Math.round(p.raised/p.goal*100);
    featured.innerHTML = `<article class="card project" style="max-width:520px;margin:0 auto">
      <div class="ratio"><img src="${p.img}" alt=""><span class="badge">${p.km} km</span></div>
      <div class="card-body">
        <h3>${t('p1_title')}</h3>
        <p style="font-size:.9rem">${t('p1_desc')}</p>
        <div class="progress"><div style="width:${pct}%"></div></div>
        <div class="meta"><span>${fmt(p.raised)} / ${fmt(p.goal)} ${t('currency')}</span><span>${pct}%</span></div>
        <a class="btn btn-gold" href="donate.html" style="margin-top:14px;width:100%">${t('cta_donate')}</a>
      </div>
    </article>`;
  }
}

let liveValue = 4280000;
function renderLive(){
  const el = document.getElementById("live-amount");
  if(!el) return;
  el.textContent = fmt(liveValue) + " " + t("currency");
}
function startLive(){
  if(!document.getElementById("live-amount")) return;
  setInterval(()=>{ liveValue += Math.floor(Math.random()*450)+50; renderLive(); }, 2500);
}

function initLangToggle(){
  const b = document.getElementById("lang-btn");
  if(b) b.addEventListener("click", ()=> setLang(getLang()==="ar"?"en":"ar"));
}

/* ---- Donate page ---- */
function initDonate(){
  const form = document.getElementById("donate-form");
  if(!form) return;
  let state = { amount:500, custom:"", freq:"once", pay:"card" };

  function paint(){
    form.querySelectorAll("[data-amt]").forEach(b=>{
      b.classList.toggle("active", !state.custom && Number(b.dataset.amt)===state.amount);
    });
    form.querySelectorAll("[data-freq]").forEach(b=> b.classList.toggle("active", b.dataset.freq===state.freq));
    form.querySelectorAll("[data-pay]").forEach(b=> b.classList.toggle("active", b.dataset.pay===state.pay));
    const total = state.custom ? Number(state.custom) : state.amount;
    document.getElementById("submit-label").textContent = `${t('submit_donation')} — ${fmt(total||0)} ${t('currency')}`;
  }

  form.addEventListener("click", e=>{
    const t = e.target.closest("button"); if(!t) return;
    if(t.dataset.amt){ state.amount = Number(t.dataset.amt); state.custom=""; document.getElementById("custom-amount").value=""; }
    if(t.dataset.freq) state.freq = t.dataset.freq;
    if(t.dataset.pay) state.pay = t.dataset.pay;
    paint();
  });
  document.getElementById("custom-amount").addEventListener("input", e=>{ state.custom=e.target.value; paint(); });

  form.addEventListener("submit", e=>{
    e.preventDefault();
    const total = state.custom ? Number(state.custom) : state.amount;
    const r = document.getElementById("receipt");
    r.style.display="block";
    document.getElementById("receipt-amount").textContent = `${fmt(total)} ${t('currency')} • ${t(state.freq)}`;
    window.scrollTo({top:0, behavior:"smooth"});
  });
  paint();
}

document.addEventListener("DOMContentLoaded", ()=>{
  initLangToggle();
  applyLang();
  startLive();
  initDonate();
});
